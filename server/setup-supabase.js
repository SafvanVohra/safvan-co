import "dotenv/config";
import { createClient } from "@supabase/supabase-js";

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !supabaseKey) {
  console.error("❌ Supabase environment variables are missing!");
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseKey, {
  auth: { persistSession: false },
});

async function setupDatabase() {
  try {
    console.log("🔄 Testing Supabase connection...");

    // Test connection by trying to query an existing table
    const { data, error: testError } = await supabase
      .from("contact_enquiries")
      .select("count", { count: "exact", head: true });

    if (testError && testError.code === "PGRST116") {
      // Table doesn't exist - provide instructions
      console.log("\n❌ Table 'contact_enquiries' does not exist yet.\n");
      console.log("📋 Please run this SQL in your Supabase dashboard (SQL Editor):\n");
      console.log("---START SQL---");
      console.log(`
create table if not exists public.contact_enquiries (
  id bigint generated by default as identity primary key,
  full_name text not null,
  email text not null,
  phone text not null,
  service text not null,
  message text not null,
  source_page text default 'react-contact-form',
  created_at timestamptz not null default now()
);

alter table public.contact_enquiries enable row level security;

drop policy if exists "Allow public enquiry inserts" on public.contact_enquiries;

create policy "Allow public enquiry inserts"
on public.contact_enquiries
for insert
to anon
with check (true);

grant insert on public.contact_enquiries to anon, authenticated;
      `);
      console.log("---END SQL---\n");
      console.log("Steps:");
      console.log("1. Go to https://app.supabase.com");
      console.log("2. Select your project");
      console.log("3. Click 'SQL Editor' in the left sidebar");
      console.log("4. Click 'New Query'");
      console.log("5. Paste the SQL above");
      console.log("6. Click 'Run'");
      console.log("7. Run this script again to verify\n");
      process.exit(1);
    } else if (testError) {
      console.error("❌ Connection error:", testError.message);
      process.exit(1);
    }

    console.log("✅ Supabase connection successful!");
    console.log("✅ Table 'contact_enquiries' exists and is accessible!");
    console.log("\n🎉 Your backend is ready to save enquiries!\n");
    process.exit(0);
  } catch (error) {
    console.error("❌ Error:", error.message);
    process.exit(1);
  }
}

setupDatabase();
